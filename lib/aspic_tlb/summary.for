      SUBROUTINE SUMMARY(N,M,CX,CY)
      IMPLICIT REAL*8 (A-H,O-Z)
      COMMON/CAL/Y2(100),X2(100),X1(100),Y1(100),IPASS,IMAP
      DIMENSION CX(8),CY(8)
   69 WRITE(7,230)
  230 FORMAT(1H1,37X,'SUMMARY TABLE (FUNCTION VALUES)',/,
     11H0,7X,'OBJECT COORDINATES',12X,'COORDINATES AFTER',
     2' TRANSFORMATION',12X,'IMAGE COORDINATES',/,
     31H0,7X,'X',16X,'Y',19X,'X',16X,'Y',19X,'X',16X,'Y',/,1H0)
      DO 231 I=1,M
        XTRAN=X1(I)+CX(1)+CX(2)*X1(I)+CX(3)*Y1(I)
        YTRAN=Y1(I)+CY(1)+CY(2)*Y1(I)+CY(3)*X1(I)
        IF(N.EQ.1)GOTO 232
        XSQ=X1(I)*X1(I)
        YSQ=Y1(I)*Y1(I)
        XTRAN=XTRAN+CX(4)*XSQ+CX(5)*X1(I)*Y1(I)+CX(6)*YSQ
        YTRAN=YTRAN+CY(4)*YSQ+CY(5)*Y1(I)*X1(I)+CY(6)*XSQ
        IF(N.EQ.2)GOTO 232
        XTRAN=XTRAN+CX(7)*(XSQ+YSQ)+CX(8)*(XSQ+YSQ)*(XSQ+YSQ)
        YTRAN=YTRAN+CY(7)*(YSQ+XSQ)+CY(8)*(YSQ+XSQ)*(YSQ+XSQ)
  232   WRITE(7,233)X1(I),Y1(I),XTRAN,YTRAN,X2(I),Y2(I)
  233 FORMAT(1H ,3(E15.7,2X,E15.7,5X))
  231 CONTINUE
      RETURN
      END
