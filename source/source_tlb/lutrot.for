	CALL SRINIT(0,.FALSE.,JSTAT)
	IF (JSTAT.NE.0) THEN
		CALL WRERR('NOARGS')
	ELSE
		CALL ASP_LUTROT(OFFSET)
		CALL WRKEYI('OFFSET',OFFSET,1,STATUS)
	ENDIF

	END
